class RaceCar:
    def __init__(self, make, model, top_speed, horsepower, weight,
                 fuel_tank_capacity, average_fuel_consumption,lap_time):
        self.make = make
        self.model = model
        self.top_speed = top_speed
        self.horsepower = horsepower
        self.weight = weight
        self.fuel_tank_capacity = fuel_tank_capacity
        self.average_fuel_consumption = average_fuel_consumption
        self.lap_time = lap_time
       